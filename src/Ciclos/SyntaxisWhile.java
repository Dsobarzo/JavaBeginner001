package Ciclos;

public class SyntaxisWhile {

    static public void main (String [] arg){

        /*
        * while(condiciones){
        *
        *   instrucciones
        *
        * } */

        int c = 1;
        while(c <= 10){

          System.out.println(c + " Hola Mundo");
          c++;

        }

    }

}
